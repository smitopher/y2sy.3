package com.cmfirsttech.y2.api.service.impl;

public class JT400Exception extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JT400Exception() {
		super();
	}

	public JT400Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public JT400Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public JT400Exception(String message) {
		super(message);
	}

	public JT400Exception(Throwable cause) {
		super(cause);
	}

}
